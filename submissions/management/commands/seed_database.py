User = get_user_model()


class Command(BaseCommand):
    help = 'Seeds the database with demo data for testing and presentations'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Clear existing data before seeding',
        )
        parser.add_argument(
            '--users',
            type=int,
            default=200,
            help='Number of users to create (default: 200)',
        )
        parser.add_argument(
            '--classrooms',
            type=int,
            default=25,
            help='Number of classrooms to create (default: 25)',
        )
        parser.add_argument(
            '--submissions-per-classroom',
            type=int,
            default=15,
            help='Average submissions per classroom (default: 15)',
        )

    def handle(self, *args, **options):
        if options['clear']:
            self.stdout.write(self.style.WARNING('Clearing existing data...'))
            self.clear_data()

        self.stdout.write(self.style.SUCCESS('Starting database seeding...'))

        # Create users
        teachers, students = self.create_users(options['users'])
        self.stdout.write(self.style.SUCCESS(
            f'âœ“ Created {len(teachers)} teachers and {len(students)} students'
        ))

        # Create classrooms
        classrooms = self.create_classrooms(teachers, options['classrooms'])
        self.stdout.write(self.style.SUCCESS(
            f'âœ“ Created {len(classrooms)} classrooms'
        ))

        # Enroll students in classrooms
        memberships = self.enroll_students(students, classrooms)
        self.stdout.write(self.style.SUCCESS(
            f'âœ“ Created {memberships} classroom memberships'
        ))

        # Create submissions
        submissions = self.create_submissions(
            students, classrooms, options['submissions_per_classroom'])
        self.stdout.write(self.style.SUCCESS(
            f'âœ“ Created {submissions} project submissions'
        ))

        # Grade some submissions
        graded = self.grade_submissions()
        self.stdout.write(self.style.SUCCESS(
            f'âœ“ Graded {graded} submissions'
        ))

        self.stdout.write(self.style.SUCCESS(
            '\nðŸŽ‰ Database seeding completed successfully!'))
        self.print_summary()

    def clear_data(self):
        """Clear existing data"""
        ProjectSubmission.objects.all().delete()
        ClassroomMembership.objects.all().delete()
        Classroom.objects.all().delete()
        User.objects.filter(is_superuser=False).delete()
        self.stdout.write(self.style.WARNING('âœ“ Cleared all existing data'))

